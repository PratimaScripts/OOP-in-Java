package unit3;

public class StringPrinter {
    public static void main(String args[]) {
        // Create a new String object with the value "Hello World"
        String quote = "Hello World";
        
        // Reassign the quote variable to a new String object with the value "kmg"
        quote = "kmg";
        
        // Print the current value of the quote variable, which is now "kmg"
        System.out.println("mutable==" + quote);
    }
}
