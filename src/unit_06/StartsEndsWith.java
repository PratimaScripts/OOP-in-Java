package unit_06;

class StartsEndsWith {
    public static void main(String args[]) {

        String str = "abcdefghijk";

        if (str.startsWith("abc"))
            System.out.println("str begins with \"abc\"");

        if (str.endsWith("ijk"))
            System.out.println("str ends with \"ijk.\"");
    }
}

		
