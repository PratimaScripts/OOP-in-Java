package unit_01;

// A simple Java program to perform subtraction of two numbers
public class Subtraction{

    // main method for which we don't to create object.
    public static void main (String[] args){

        int number1 = 10;
        int number2 = 4;

        // this will subtract number2 from number1
        int result = number1 - number2;


        /*
         Example of multi-line comment
         Prints the result.
         + is the concatenation operator here
         */
        System.out.println("The result of subtraction is: " + result);
    }
}